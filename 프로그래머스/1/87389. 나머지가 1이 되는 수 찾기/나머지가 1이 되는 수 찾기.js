function solution(n) {
    // n 을 x 로 나눈 나머지가 1이 되도록 하는 가장 작은 자연수 x
    // 1부터 n 까지 중에 나눳을때 나머지가 1인게 처음 나오는게 가장 작은 자연수
    let num = 0; // 나머지가 1인 자연수
    for (let i = 1; i < n; i++) {  // 1부터 n-1까지 확인
        if (n % i === 1) {  // 나머지가 1인 경우
            num = i;  // 가장 작은 값을 저장
            break;  // 첫 번째로 찾은 값이 가장 작은 값이므로 바로 종료
        }
    }
    return num;
}